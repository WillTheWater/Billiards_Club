void PlayState::HandleInput(Game& game)
{
	std::cin >> mInput;
	switch (mInput)
	{
	case 1:
		game.GetStateStack().PopState();
		break;
	case 2:
		game.GetStateStack().ClearStates();
		break;
	default:
		std::cout << "			Invalid input\n";
		break;
	}
}

